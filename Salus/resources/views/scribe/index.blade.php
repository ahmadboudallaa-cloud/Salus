        <h1 id="introduction">Introduction</h1>
<p>API REST pour suivre les symptomes, prendre des rendez-vous et obtenir des conseils de bien-etre generes par IA.</p>
<aside>
    <strong>Base URL</strong>: <code>http://localhost:8000</code>
</aside>
<pre><code>Cette documentation decrit les endpoints disponibles, les formats de reponses et la methode d'authentification.

&lt;aside&gt;Les routes protegees exigent un header Authorization avec un token Sanctum: &lt;code&gt;Authorization: Bearer {token}&lt;/code&gt;.&lt;/aside&gt;</code></pre>

        <h1 id="authenticating-requests">Authenticating requests</h1>
<p>To authenticate requests, include an <strong><code>Authorization</code></strong> header with the value <strong><code>"Bearer Bearer {token}"</code></strong>.</p>
<p>All authenticated endpoints are marked with a <code>requires authentication</code> badge in the documentation below.</p>
<p>Recuperez un token via /api/login puis utilisez-le dans le header Authorization.</p>

